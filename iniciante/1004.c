#include <stdio.h>

int main() {

    // Declarando três variáveis de valores inteiros 
    int a, b, prod;
    // Lendo os valores das duas primeiras variáveis 
    scanf("%d %d", &a, &b);
    
    // Afirmando que o valor do "prod" é o produto entre as duas primeiras variáveis
    prod = a * b;

    // Exibindo a mensagem requisitada 
    printf("PROD = %d\n", prod);

    return 0;
}
