#include <stdio.h>

int main() {

    // Declarando três variáveis de valores inteiros 
    int a, b, soma;
    // Lendo os valores de "a" e de "b"
    scanf("%d %d", &a, &b);

    // Afirmando que a variável "soma" tem o valor da soma das outras duas variáveis 
    soma = a + b;

    // Exibindo a mensagem requisitada 
    printf("SOMA = %d\n", soma);

    return 0;
}
